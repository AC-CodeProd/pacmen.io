Game.prototype.onDebugPaths = function() {
    this.ctxBoard.strokeStyle = "red";
    for (var i = 0, imax = this.PATHS.length; i < imax; i++) {
        var p = this.PATHS[i];
        var startX = p.split("-")[0].split(",")[0];
        var startY = p.split("-")[0].split(",")[1];
        var endX = p.split("-")[1].split(",")[0];
        var endY = p.split("-")[1].split(",")[1];
        this.ctxBoard.beginPath();
        this.ctxBoard.moveTo(startX, startY);
        this.ctxBoard.lineTo(endX, endY);
        this.ctxBoard.stroke();
        this.ctxBoard.closePath();
    }
};